#!/usr/bin/env node

/**
 * Auto-generates button variants from theme.css acc-button classes
 *
 * Usage: node scripts/generate-button-variants.js
 *
 * This script:
 * 1. Reads theme.css and extracts .{class} .acc-button--link classes
 * 2. Generates button variants based on those classes
 * 3. Creates button-variants.js files
 * 4. Updates component models with variant options
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { execSync } from 'child_process';

/* eslint-disable no-underscore-dangle */
const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
/* eslint-enable no-underscore-dangle */

const THEME_CSS_PATH = path.join(__dirname, '../styles/theme.css');
const BANNER_VARIANTS_PATH = path.join(__dirname, '../blocks/banner/button-variants.js');
const CTA_VARIANTS_PATH = path.join(__dirname, '../blocks/cta/button-variants.js');
const MEET_THE_TEAM_VARIANTS_PATH = path.join(__dirname, '../blocks/meet-the-team/button-variants.js');
const BANNER_MODEL_PATH = path.join(__dirname, '../models/_banner.json');
const CTA_MODEL_PATH = path.join(__dirname, '../models/_cta.json');
const MEET_THE_TEAM_MODEL_PATH = path.join(__dirname, '../models/_meet-the-team.json');

/**
 * Parse button classes from theme.css
 * Looks for patterns like: .purple .acc-button--link {
 */
function parseButtonClasses(cssContent) {
  const classes = [];
  const classRegex = /^\.([a-z-]+) \.acc-button--link \{/gm;

  let match;
  // eslint-disable-next-line no-cond-assign
  while ((match = classRegex.exec(cssContent)) !== null) {
    const [, className] = match;
    classes.push(className);
  }

  return classes;
}

/**
 * Convert class name to human-readable title
 * Examples:
 *   purple -> Purple
 *   white-outline -> White Outline
 *   orange-underline -> Orange Underline
 */
function classToTitle(className) {
  return className
    .split('-')
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');
}

/**
 * Generate button variant configurations
 */
function generateButtonVariants(classes) {
  const variants = {};

  classes.forEach((className, index) => {
    variants[className] = {
      componentVariantTitle: classToTitle(className),
      // First variant is the default
      ...(index === 0 ? { componentVariantDefault: 'true' } : {}),
      className,
    };
  });

  return variants;
}

/**
 * Generate JavaScript file content
 */
function generateVariantsJS(variants) {
  const variantsJSON = JSON.stringify(variants, null, 2)
    .replace(/"([^"]+)":/g, '"$1":'); // Keep property names quoted

  return `/* eslint-disable */
/**
 * Button variants configuration
 * AUTO-GENERATED by scripts/generate-button-variants.js
 * DO NOT EDIT MANUALLY - changes will be overwritten
 *
 * To update: npm run build:variants
 *
 * These variants use CSS classes from styles/theme.css
 * Each variant wraps the button in a div with the specified className
 */
export default function getButtonVariants() {
  return ${variantsJSON};
}
`;
}

/**
 * Generate model options for Universal Editor
 */
function generateModelOptions(variants) {
  return Object.keys(variants).map((key) => ({
    name: variants[key].componentVariantTitle,
    value: `[variant-${key}]`,
  }));
}

/**
 * Update model JSON file with new variant options
 */
function updateModelFile(modelPath, options) {
  // Read existing model
  const modelContent = fs.readFileSync(modelPath, 'utf-8');
  const model = JSON.parse(modelContent);

  // Find variant fields and update their options
  model.fields.forEach((field) => {
    if (field.name === 'cta1Variant' || field.name === 'ctaVariant') {
      // Primary button or single button - use default order
      field.options = options;
    }
    if (field.name === 'cta2Variant') {
      // For secondary button, rotate the list to make outline the default
      const outlineOption = options.find((opt) => opt.name.toLowerCase().includes('outline'));
      if (outlineOption) {
        const reordered = [outlineOption, ...options.filter((opt) => opt !== outlineOption)];
        field.options = reordered;
      } else {
        field.options = options;
      }
    }
  });

  // Write back to file with pretty formatting
  fs.writeFileSync(modelPath, JSON.stringify(model, null, 2));
}

/**
 * Main execution
 */
function main() {
  // eslint-disable-next-line no-console
  console.log('🎨 Generating button variants from theme.css acc-button classes...\n');

  // Read theme.css
  const themeCSS = fs.readFileSync(THEME_CSS_PATH, 'utf-8');
  // eslint-disable-next-line no-console
  console.log('✅ Read theme.css');

  // Parse button classes
  const classes = parseButtonClasses(themeCSS);
  // eslint-disable-next-line no-console
  console.log(`✅ Found ${classes.length} acc-button classes`);
  // eslint-disable-next-line no-console
  console.log(`   Classes: ${classes.join(', ')}`);

  // Generate variants
  const variants = generateButtonVariants(classes);
  // eslint-disable-next-line no-console
  console.log(`\n✅ Generated ${Object.keys(variants).length} button variants`);

  // Generate JavaScript files
  const jsContent = generateVariantsJS(variants);
  fs.writeFileSync(BANNER_VARIANTS_PATH, jsContent);
  fs.writeFileSync(CTA_VARIANTS_PATH, jsContent);
  fs.writeFileSync(MEET_THE_TEAM_VARIANTS_PATH, jsContent);
  // eslint-disable-next-line no-console
  console.log('✅ Written button-variants.js files');

  // Generate model options
  const options = generateModelOptions(variants);
  // eslint-disable-next-line no-console
  console.log(`\n✅ Generated ${options.length} model options`);
  // eslint-disable-next-line no-console
  console.log('\n📋 Available variants:');
  options.forEach((opt) => {
    // eslint-disable-next-line no-console
    console.log(`   - ${opt.name}`);
  });

  // Update model files
  // eslint-disable-next-line no-console
  console.log('\n📝 Updating model files...');
  updateModelFile(BANNER_MODEL_PATH, options);
  updateModelFile(CTA_MODEL_PATH, options);
  updateModelFile(MEET_THE_TEAM_MODEL_PATH, options);
  // eslint-disable-next-line no-console
  console.log('✅ Updated models/_banner.json, models/_cta.json, and models/_meet-the-team.json');

  // Run build:json to regenerate component-models.json
  // eslint-disable-next-line no-console
  console.log('\n🔨 Running npm run build:json...');
  try {
    execSync('npm run build:json', { stdio: 'inherit', cwd: path.join(__dirname, '..') });
    // eslint-disable-next-line no-console
    console.log('✅ Built component-models.json');
  } catch (error) {
    // eslint-disable-next-line no-console
    console.error('❌ Failed to build JSON files:', error.message);
  }

  // eslint-disable-next-line no-console
  console.log('\n✅ Done! Button variants generated and models updated successfully.');
  // eslint-disable-next-line no-console
  console.log('\n📦 Files updated:');
  // eslint-disable-next-line no-console
  console.log('   - blocks/banner/button-variants.js');
  // eslint-disable-next-line no-console
  console.log('   - blocks/cta/button-variants.js');
  // eslint-disable-next-line no-console
  console.log('   - blocks/meet-the-team/button-variants.js');
  // eslint-disable-next-line no-console
  console.log('   - models/_banner.json');
  // eslint-disable-next-line no-console
  console.log('   - models/_cta.json');
  // eslint-disable-next-line no-console
  console.log('   - models/_meet-the-team.json');
  // eslint-disable-next-line no-console
  console.log('   - component-models.json');
}

main();
